# Vamos receber dois dados diferentes do usuário e concatena-los em uma única string?!

# Solicita ao usuário que insira o primeiro dado
dado1 = input("Por favor, insira o primeiro dado: ")

# Solicita ao usuário que insira o segundo dado
dado2 = input("Por favor, insira o segundo dado: ")

# Concatena os dois dados em uma única string com um espaço entre eles
resultado = dado1 + " " + dado2

# Exibe o resultado
print("A string concatenada é:", resultado)
